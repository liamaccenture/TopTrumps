
public class Card {

	String m_name;
	String m_imageLoc;
	
	int m_speed;
	int m_handling;
	int m_function;
	int m_practicality;
	int m_price;
	
	public Card(String name, String imageLoc, int speed, int handling, int function, int practicality, int price) {
		
		m_name = name;
		m_imageLoc = imageLoc;
		m_speed = speed;
		m_handling = handling;
		m_function = function;
		m_practicality = practicality;
		m_price = price;
		
	}
	
}
